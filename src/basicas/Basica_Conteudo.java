package basicas;

public class Basica_Conteudo {
	
	public String Data;
	
	public String Texto;
	
	public String Autor;
	
	public String getData() {
		return Data;
	}

	public void setData(String data) {
		Data = data;
	}

	public String getTexto() {
		return Texto;
	}

	public void setTexto(String texto) {
		Texto = texto;
	}

	public String getAutor() {
		return Autor;
	}

	public void setAutor(String autor) {
		Autor = autor;
	}

	

}
